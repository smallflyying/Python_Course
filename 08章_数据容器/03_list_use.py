# 列表的使用语法
list2 = ['red', 'green', 'blue', 'yellow', 'white', 'black']
# 列出所有的数据
print(list2)  # ['red', 'green', 'blue', 'yellow', 'white', 'black']

# 访问/获取第三个数据/元素
print("第三个元素是: ", list2[2])  # blue