# 使用while完成10句 "hello, 韩顺平教育"

"""
    思路分析
    1. 定义变量 i = 1
    2. 使用while 循环判断 i <= 10, 如果为真就执行 "hello, 韩顺平教育"
    3. 注意 i 的值 要不断的增加 i += 1
"""
i = 1
while i <= 10:
    print("hello, 韩顺平教育", i)
    i += 1
