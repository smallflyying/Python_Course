# 演示记录人的信息并输出的程序

name = "tom"  # 字符串
age = 20  # 整型
score = 90.4  # 浮点型(小数)
gender = "男"  # 字符串

# 输出信息
print("个人信息如下:")
print(name)
print(age)
print(gender)
print(score)

# 输出信息 （格式化: ctrl alt + L）
print("个人信息如下:", name, age, gender, score)
