# 变量的实例

# 老师解读
# 定义了一个变量, 变量的名称a，变量的值是1，1是int类型(整数类型)
a = 1
# 定义了一个变量, 变量的名称b，变量的值是2，2是int类型(整数类型)
b = 2
# 变量b的值修改成8，变量b的值就是8，8是int类型
b = 8

# 输出变量的值
print("a的值是:", a, "类型是:", type(a))
print("b的值是:", b, "类型是:", type(b))

